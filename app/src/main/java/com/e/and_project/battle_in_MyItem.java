package com.e.and_project;



public class battle_in_MyItem {

    private String img;
    private String name;

    public battle_in_MyItem(String img, String name){
        this.img = img;
        this.name = name;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}